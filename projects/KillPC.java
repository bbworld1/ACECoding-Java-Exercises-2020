// While it does not actually kill most PCs, if you have a slow
// PC or you're using repl.it, it will probably freeze it.
//
// How It Works:
// while (condition) loops run as long as the condition is true.
// if we set condition to true, then it runs forever (because true always equals true in Java).
// Use Ctrl-C (or "Stop" button in Repl.it) to exit

public class KillPC {
    public static void main(String[] args) {
        // We're gonna do what's called a pro gamer move
        while (true) {
            System.out.println("InFiNite LooP");
        }
    }
}
